/**
 * Chrome user-profile decoration and clean-exit management.
 *
 * When launching a dedicated browser profile we want a recognizable name and
 * accent color in Chrome's profile picker.  Chrome stores this metadata in
 * two JSON files inside the user-data directory:
 *
 *   - `Local State`            (top-level profile registry)
 *   - `Default/Preferences`    (per-profile settings)
 *
 * This module reads, patches, and writes those files idempotently.
 *
 * Reference: Chromium source — `chrome/browser/profiles/profile_info_cache.cc`
 */

import fs from "node:fs";
import path from "node:path";
import { DEFAULT_BROWSER_COLOR, DEFAULT_BROWSER_PROFILE_NAME } from "../constants";

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function readJson(fp: string): Record<string, unknown> | null {
  try {
    if (!fs.existsSync(fp)) return null;
    const parsed = JSON.parse(fs.readFileSync(fp, "utf-8")) as unknown;
    if (typeof parsed !== "object" || parsed === null || Array.isArray(parsed)) return null;
    return parsed as Record<string, unknown>;
  } catch {
    return null;
  }
}

function writeJson(fp: string, data: Record<string, unknown>) {
  fs.mkdirSync(path.dirname(fp), { recursive: true });
  fs.writeFileSync(fp, JSON.stringify(data, null, 2));
}

function deepSet(obj: Record<string, unknown>, keys: string[], value: unknown) {
  let node = obj;
  for (const key of keys.slice(0, -1)) {
    const next = node[key];
    if (typeof next !== "object" || next === null || Array.isArray(next)) {
      node[key] = {};
    }
    node = node[key] as Record<string, unknown>;
  }
  node[keys[keys.length - 1] ?? ""] = value;
}

/**
 * Chrome stores SkColor values as **signed 32-bit integers** with full alpha.
 * Convert a `#RRGGBB` hex string to that representation.
 */
function hexToSignedArgb(hex: string): number | null {
  const digits = hex.trim().replace(/^#/, "");
  if (!/^[0-9a-fA-F]{6}$/.test(digits)) return null;
  const rgb = Number.parseInt(digits, 16);
  const unsigned = (0xff << 24) | rgb;
  return unsigned > 0x7fffffff ? unsigned - 0x1_0000_0000 : unsigned;
}

